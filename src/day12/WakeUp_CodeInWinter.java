package day12;

public class WakeUp_CodeInWinter {

    public static void main(String[] args) {
        String weather="Cloudy";
        if(weather.equalsIgnoreCase("Rainy")){
            System.out.println("code in rainy weather");
        }else if(weather.equalsIgnoreCase("Sunny")){
            System.out.println("code in sunny day");
        }else if(weather.equalsIgnoreCase("cloudy")){
            System.out.println("code in cloudy day");
        }else if(weather.equalsIgnoreCase("Snowy")){
            System.out.println("code in snowy day");
        }else {
            System.out.println("rain or shine just keep coding anyway");
        }







    }
}
