package day17;

public class NamePrinter_WhileLoop {

    public static void main(String[] args) {

   String name = " Zeynep";

   int x = 0;

   while (x < name.length()){
      System.out.print("index " + x + " : ");
       System.out.print(name.charAt(x));
       ++x;
   }
















    }
}
