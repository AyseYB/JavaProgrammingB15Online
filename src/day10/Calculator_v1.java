package day10;

public class Calculator_v1 {
    public static void main(String[] args) {

        char operator ='&';
        switch (operator){
            case '+':
                System.out.println(" You are about to add number");
                break;
            case'-':
                System.out.println("You are about to subtract number");
                break;
            case '*':
                System.out.println("You are about to multiply numbers");
                break;
            case '/':
                System.out.println(" You are about to divide numbers");
                break;
            default:
                System.out.println("INVALID OPERATOR");
        }















    }
}
